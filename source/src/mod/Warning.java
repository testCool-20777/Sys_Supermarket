package mod;

public class Warning {

	private Integer cno;
	private Integer cnumber;
	private Integer cwarn;
	private Integer kwarn;
	public Integer getCno() {
		return cno;
	}
	public void setCno(Integer cno) {
		this.cno = cno;
	}
	public Integer getCnumber() {
		return cnumber;
	}
	public void setCnumber(Integer cnumber) {
		this.cnumber = cnumber;
	}
	public Integer getCwarn() {
		return cwarn;
	}
	public void setCwarn(Integer cwarn) {
		this.cwarn = cwarn;
	}
	public Integer getKwarn() {
		return kwarn;
	}
	public void setKwarn(Integer kwarn) {
		this.kwarn = kwarn;
	}
	
}
